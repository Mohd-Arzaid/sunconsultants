// Reverse mapping for finding English URL from localized URL
export const REVERSE_URL_MAPPINGS = {};

// Create reverse mappings
Object.keys(URL_MAPPINGS).forEach((lang) => {
  REVERSE_URL_MAPPINGS[lang] = {};
  Object.keys(URL_MAPPINGS[lang]).forEach((englishUrl) => {
    const localizedUrl = URL_MAPPINGS[lang][englishUrl];
    REVERSE_URL_MAPPINGS[lang][localizedUrl] = englishUrl;
  });
});

/**
 * Get localized URL for a given English URL and language
 * @param {string} englishUrl - The English URL
 * @param {string} lang - The target language
 * @returns {string} The localized URL
 */
export const getLocalizedUrl = (englishUrl, lang) => {
  if (lang === "en" || !URL_MAPPINGS[lang]) {
    return englishUrl;
  }
  return URL_MAPPINGS[lang][englishUrl] || englishUrl;
};

/**
 * Get English URL from localized URL
 * @param {string} localizedUrl - The localized URL
 * @param {string} lang - The current language
 * @returns {string} The English URL
 */
export const getEnglishUrl = (localizedUrl, lang) => {
  if (lang === "en" || !REVERSE_URL_MAPPINGS[lang]) {
    return localizedUrl;
  }
  return REVERSE_URL_MAPPINGS[lang][localizedUrl] || localizedUrl;
};

/**
 * Generate full localized path with language prefix
 * @param {string} englishUrl - The English URL
 * @param {string} lang - The target language
 * @returns {string} The full localized path
 */
export const getFullLocalizedPath = (englishUrl, lang) => {
  if (lang === "en") {
    return `/${englishUrl}`;
  }
  const localizedUrl = getLocalizedUrl(englishUrl, lang);
  return `/${lang}/${localizedUrl}`;
};

/**
 * Get all available languages for a URL
 * @param {string} englishUrl - The English URL
 * @returns {Object} Object with language codes as keys and localized URLs as values
 */
export const getAllLocalizedUrls = (englishUrl) => {
  const result = {};
  Object.keys(URL_MAPPINGS).forEach((lang) => {
    result[lang] = getFullLocalizedPath(englishUrl, lang);
  });
  return result;
};
